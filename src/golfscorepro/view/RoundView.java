    private void loadRoundsToTable() {
        DefaultTableModel model = (DefaultTableModel) tblRounds.getModel();
        model.setRowCount(0);

        for (Round r : GolfClub.getRounds()) {
            Object[] row = { r.getRoundId(), r.getGolferId(), r.getCourseName(), r.getDate(), r.getTotalScore() };
            model.addRow(row);
        }
    }

