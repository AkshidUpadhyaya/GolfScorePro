    private void btnAddRoundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddRoundActionPerformed
        String roundId = txtRoundId.getText().trim();
        String golferId = txtRoundGolferId.getText().trim();
        String course = txtCourseName.getText().trim();
        String date = txtRoundDate.getText().trim();
        String scoreText = txtTotalScore.getText().trim();

        if (roundId.isEmpty() || golferId.isEmpty() || course.isEmpty() || date.isEmpty() || scoreText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        if (!date.matches("\\d{4}-\\d{2}-\\d{2}")) {
            JOptionPane.showMessageDialog(this, "Date must be in YYYY-MM-DD format.");
            return;
        }

        int score;
        try {
            score = Integer.parseInt(scoreText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Total Score must be a number.");
            return;
        }

        if (score < 0 || score > 200) {
            JOptionPane.showMessageDialog(this, "Total Score must be between 0 and 200.");
            return;
        }

        for (Round r : GolfClub.getRounds()) {
            if (r.getRoundId().equalsIgnoreCase(roundId)) {
                JOptionPane.showMessageDialog(this, "Duplicate Round ID. Please use a unique ID.");
                return;
            }
        }

        Round newRound = new Round(roundId, golferId, course, date, score);
        GolfClub.addRound(newRound);
        RecentRoundQueue.addRound(newRound);

        loadRoundsToTable();

        txtRoundId.setText("");
        txtRoundGolferId.setText("");
        txtCourseName.setText("");
        txtRoundDate.setText("");
        txtTotalScore.setText("");

        JOptionPane.showMessageDialog(this, "Round added successfully!");

        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddRoundActionPerformed

    private void btnDeleteRoundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRoundActionPerformed
        int selectedRow = tblRounds.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a round to delete.");
            return;
        }

        GolfClub.removeRound(selectedRow);
        loadRoundsToTable();
        JOptionPane.showMessageDialog(this, "Round deleted successfully.");

        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteRoundActionPerformed

