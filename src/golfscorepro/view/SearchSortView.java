    private void btnSearchIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchIdActionPerformed
        String id = txtSearchId.getText().trim();

        if (id.isEmpty()) {
            showError("Please enter a Golfer ID to search.");
            return;
        }

       

        if (GolfClub.getGolfers().isEmpty()) {
            showError("No golfers available to search.");
            return;
        }

        bubbleSortById(); // MUST sort before binary search
        Golfer result = binarySearchById(id);

        DefaultTableModel model = (DefaultTableModel) tblResults.getModel();
        model.setRowCount(0);

        if (result != null) {
            Object[] row = { result.getGolferId(), result.getName(), result.getHandicap() };
            model.addRow(row);
        } else {
            JOptionPane.showMessageDialog(this, "Golfer ID not found: " + id);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnSearchIdActionPerformed

