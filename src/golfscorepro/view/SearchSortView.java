    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String keyword = txtSearchName.getText().trim();

        if (keyword.isEmpty()) {
            showError("Please enter a name to search.");
            return;
        }

        if (keyword.length() < 2) {
            showError("Please enter at least 2 characters for name search.");
            return;
        }

        keyword = keyword.toLowerCase();

        DefaultTableModel model = (DefaultTableModel) tblResults.getModel();
        model.setRowCount(0);

        int count = 0;
        for (Golfer g : GolfClub.getGolfers()) {
            if (g.getName().toLowerCase().contains(keyword)) {
                Object[] row = { g.getGolferId(), g.getName(), g.getHandicap() };
                model.addRow(row);
                count++;
            }
        }

        if (count == 0) {
            JOptionPane.showMessageDialog(this, "No golfers found for: " + txtSearchName.getText().trim());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

