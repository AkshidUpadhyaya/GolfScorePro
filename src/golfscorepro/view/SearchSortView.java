    private void bubbleSortById() {
        int n = GolfClub.getGolfers().size();

        for (int i = 0; i < n - 1; i++) {
            for (int j = 0; j < n - i - 1; j++) {
                if (GolfClub.getGolfers().get(j).getGolferId()
                    .compareToIgnoreCase(GolfClub.getGolfers().get(j + 1).getGolferId()) > 0) {

                    Golfer temp = GolfClub.getGolfers().get(j);
                    GolfClub.getGolfers().set(j, GolfClub.getGolfers().get(j + 1));
                    GolfClub.getGolfers().set(j + 1, temp);
                }
            }
        }
    }
    
    private Golfer binarySearchById(String id) {
        int low = 0;
        int high = GolfClub.getGolfers().size() - 1;

        while (low <= high) {
            int mid = (low + high) / 2;
            Golfer g = GolfClub.getGolfers().get(mid);
            int result = g.getGolferId().compareToIgnoreCase(id);

            if (result == 0) return g;
            else if (result < 0) low = mid + 1;
            else high = mid - 1;
        }
        return null;
    }


    
