    private boolean isValidGolferIdFormat(String id) {
        // Simple rule: must not contain spaces, must be at least 2 chars
        if (id == null) return false;
        id = id.trim();
        if (id.length() < 2) return false;
        return !id.contains(" ");
    }


    private void bubbleSortByHandicapAsc() {
        int n = GolfClub.getGolfers().size();

        for (int i = 0; i < n - 1; i++) {
            for (int j = 0; j < n - i - 1; j++) {
                if (GolfClub.getGolfers().get(j).getHandicap() >
                    GolfClub.getGolfers().get(j + 1).getHandicap()) {

                    Golfer temp = GolfClub.getGolfers().get(j);
                    GolfClub.getGolfers().set(j, GolfClub.getGolfers().get(j + 1));
                    GolfClub.getGolfers().set(j + 1, temp);
                }
            }
        }
    }
    
