    private void btnAddGolferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddGolferActionPerformed
        String id = txtGolferId.getText().trim();
        String name = txtName.getText().trim();
        String handicapText = txtHandicap.getText().trim();

        // Basic validation
        if (id.isEmpty() || name.isEmpty() || handicapText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        int handicap;
        try {
            handicap = Integer.parseInt(handicapText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Handicap must be a number.");
            return;
        }

        if (handicap < 0 || handicap > 54) {
            JOptionPane.showMessageDialog(this, "Handicap must be between 0 and 54.");
            return;
        }

        // Duplicate ID check
        for (Golfer g : GolfClub.getGolfers()) {
            if (g.getGolferId().equalsIgnoreCase(id)) {
                JOptionPane.showMessageDialog(this, "Duplicate Golfer ID. Please use a unique ID.");
                return;
            }
        }

        // Save golfer
        Golfer newGolfer = new Golfer(id, name, handicap);
        GolfClub.addGolfer(newGolfer);
        
        RecentGolferQueue.addGolfer(newGolfer);

        // Refresh UI
        loadGolfersToTable();

        // Clear inputs
        txtGolferId.setText("");
        txtName.setText("");
        txtHandicap.setText("");

        JOptionPane.showMessageDialog(this, "Golfer added successfully!");
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddGolferActionPerformed

    private void btnDeleteGolferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteGolferActionPerformed
        int selectedRow = tblGolfers.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a golfer to delete.");
        return;
        }

        Golfer removedGolfer = GolfClub.getGolfers().remove(selectedRow);

        // Push to stack for undo
        UndoStack.push(removedGolfer);

        loadGolfersToTable();

        JOptionPane.showMessageDialog(this, "Golfer deleted. You can undo this action.");
        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteGolferActionPerformed

    private void btnUndoDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUndoDeleteActionPerformed
        if (UndoStack.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nothing to undo.");
        return;
        }

        Golfer restoredGolfer = UndoStack.pop();
        GolfClub.addGolfer(restoredGolfer);

        loadGolfersToTable();

        JOptionPane.showMessageDialog(this, "Golfer restored successfully.");

        // TODO add your handling code here:
    }//GEN-LAST:event_btnUndoDeleteActionPerformed

