    private void loadGolfersToTable() {
        DefaultTableModel model = (DefaultTableModel) tblGolfers.getModel();
        model.setRowCount(0); // clear table

        for (Golfer g : GolfClub.getGolfers()) {
            Object[] row = { g.getGolferId(), g.getName(), g.getHandicap() };
            model.addRow(row);
        }
    }

